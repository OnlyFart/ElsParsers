namespace Parser.Core {
    public class Book {
        /// <summary>
        /// Идентификатор книги
        /// </summary>
        public long Id;
        
        /// <summary>
        /// Авторы кники
        /// </summary>
        public string Authors;
        
        /// <summary>
        /// ISBN
        /// </summary>
        public string ISBN;
        
        /// <summary>
        /// ISSN
        /// </summary>
        public string ISSN;
        
        /// <summary>
        /// Издательство
        /// </summary>
        public string Publisher;
        
        /// <summary>
        /// Название
        /// </summary>
        public string Name;
        
        /// <summary>
        /// Год издания
        /// </summary>
        public string Year;
        
        /// <summary>
        /// Библиографическое описание
        /// </summary>
        public string Bib;
        
        /// <summary>
        /// Кол-во страниц
        /// </summary>
        public int Pages;
    }
}
